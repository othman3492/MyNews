package com.example.android.mynews.Models;



public class Article {

    private String name;
    private int date;



    Article() {

    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }
}
